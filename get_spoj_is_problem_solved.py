import bs4 as bs
import requests

for problem_link in list_of_prblem_links:
    status_link = problem_link.replace("problems", "status")
    status_link = status_link[:-1] + ",mostafa_36a2" + "/"
    response = requests.get(status_link)
    soup = bs.BeautifulSoup(str(response.content), "lxml")
    strongs = soup.findAll("strong")

    Solved = False
    for strong in strongs:
        if strong.text == "accepted":
            Solved = True

    if Solved:
        print("Solved")
    else:
        print("Not solved yet.")
